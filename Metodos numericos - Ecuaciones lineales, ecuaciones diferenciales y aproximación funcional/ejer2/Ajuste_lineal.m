% Aproximación Lineal por Mínimos Cuadrados:

function [A,B,R]=Ajuste_lineal(x,y)

if nargin~=2
    disp('debe ingresar dos argumentos')
    return;
end

%valores medios
xprom=mean(x);
yprom=mean(y);
C=x-xprom;
D=y-yprom;
A=(C*D')/(C*C');
B=yprom-A*xprom;

%coeficiente de correlacion
R=(C*D')/((C*C')*(D*D')).^0.5;












































































































































































  
    